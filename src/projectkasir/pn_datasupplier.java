    private void load_table(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No.");
        model.addColumn("Kode Supplier");
        model.addColumn("Nama Supplier");
        model.addColumn("Alamat");
        model.addColumn("Telepon");
        
        String cari = txtpencarian.getText();
        try {
            int no=1;
            String sql = ("SELECT * FROM supplier WHERE id_supplier LIKE '%" + cari + "%' OR nama_supplier LIKE '%" + cari + "%'"
                    + "OR alamat_supplier LIKE '%" + cari + "%' OR telp_supplier LIKE '%" + cari + "%'");
            Connection conn= Config.configDB();
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet res=stm.executeQuery(sql);
            while(res.next()) {
                model.addRow(new Object[]{no++,res.getString(1),res.getString(2),res.getString(3),res.getString(4)});
            }
            tblsupplier.setModel(model);
        } catch (Exception e) {
            System.out.print(e.getMessage());
        }
    }
     
