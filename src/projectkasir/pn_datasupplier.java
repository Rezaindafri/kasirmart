    private void autonumber() {
        try {
            String sql = "SELECT MAX(RIGHT(id_supplier,4)) AS no_auto FROM supplier";
            Connection conn= Config.configDB();
            java.sql.Statement stm= conn.createStatement();
            ResultSet res = stm.executeQuery(sql);
            if (res.next()) {
                String no_auto, nol_plus;
                int p;
                no_auto = Integer.toString(res.getInt(1)+1);
                p = no_auto.length();
                nol_plus = "";
                for(int i=1;i<=4-p;i++){
                    nol_plus = nol_plus +"0";
                }
                txtkdsupplier.setText("P"+nol_plus+no_auto);
            }
        } catch (Exception e) {
            txtkdsupplier.setText("P0001");
        }
        txtkdsupplier.disable();
    }
