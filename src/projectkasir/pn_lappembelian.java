    private void load_table(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No.");
        model.addColumn("No. Pembelian");
        model.addColumn("Kode Supplier");
        model.addColumn("Nama Supplier");
        model.addColumn("Kode Barang");
        model.addColumn("Nama Barang");
        model.addColumn("Harga Barang");
        model.addColumn("Jumlah");
        model.addColumn("Subtotal");
        model.addColumn("Tanggal");
        
        String cari = txtpencarian.getText();
        String cari2 = txtpencarian2.getText();
        try {
            int no=1;
            String query = "SELECT pembelian.id_pembelian, detail_pembelian.id_supplier, detail_pembelian.nama_supplier, detail_pembelian.id_barang, detail_pembelian.nama_barang, detail_pembelian.harga_barang, detail_pembelian.jumlah_barang, detail_pembelian.subtotal, pembelian.tgl_pembelian " +
                "FROM pembelian INNER JOIN detail_pembelian ON pembelian.id_pembelian = detail_pembelian.id_pembelian " + "WHERE pembelian.tgl_pembelian LIKE '%" + cari + "%'" + " OR pembelian.tgl_pembelian BETWEEN '" + cari + "' AND '" + cari2 + "'";
            Connection conn= Config.configDB();
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet res=stm.executeQuery(query);
            while(res.next()) {
                model.addRow(new Object[]{no++,res.getString(1),res.getString(2),res.getString(3),res.getString(4),res.getString(5),res.getString(6),res.getString(7),res.getString(8),res.getString(9)});
            }
            tbllappembelian.setModel(model);
            tbllappembelian.disable();
        } catch (Exception e) {
            System.out.print(e.getMessage());
        }
        setColumnWidth();
    }
    
