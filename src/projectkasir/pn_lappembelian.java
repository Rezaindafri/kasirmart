        private String jml_pengeluaran(String text)throws SQLException {
            String cari = txtpencarian.getText();
            String cari2 = txtpencarian2.getText();
            String sql = "SELECT SUM(pembelian.total) AS total FROM pembelian WHERE pembelian.tgl_pembelian LIKE '%" + cari + "%'" + " OR pembelian.tgl_pembelian BETWEEN '" + cari + "' AND '" + cari2 + "'";
            java.sql.Statement stm = conn.createStatement();
            ResultSet res = stm.executeQuery(sql);
            
            res.next();
            int jumlah = res.getInt("total");
            
            Label lbl_totalP = new Label("total pengeluaran: ");
            String data_jumlah = formatRupiah(jumlah);

            return data_jumlah;
        }
        private String formatRupiah(int amount) {
        // Membuat objek NumberFormat dengan format mata uang Rupiah
        NumberFormat rupiahFormat = new DecimalFormat("#,###,###,###");

        // Menggunakan metode format untuk mengonversi nilai ke format Rupiah
        return "Rp " + rupiahFormat.format(amount);
        }
